import SwiftUI

// MARK: - EATME Color Palette
extension UIColor {
    enum EATME {
        static let Black = UIColor(named: "Black") ?? UIColor.black
        static let Blue = UIColor(named: "Blue") ?? UIColor.blue
        static let DarkGray = UIColor(named: "DarkGray") ?? UIColor.darkGray
        static let Gray = UIColor(named: "Gray") ?? UIColor.gray
        static let LightGray = UIColor(named: "LightGray") ?? UIColor.lightGray
        static let White = UIColor(named: "White") ?? UIColor.white
    }
}
